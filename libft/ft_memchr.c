#include "libft.h"

void	*ft_memchr(const void *b, int c, size_t len)
{
	size_t	i;

	i = 0;
	while (i < len)
	{
		if (*(unsigned char *)(b + i) == (unsigned char)c)
			return ((void *)(b + i));
		i++;
	}
	return (NULL);
}
